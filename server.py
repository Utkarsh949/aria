def _open_browser(url: str, delay: float = 1.2) -> None:
    """
    Open the browser after a short delay without flashing a CMD window.
    Uses os.startfile on Windows (no subprocess visible), subprocess on macOS/Linux.
    """
    import platform, subprocess, time as _t

    def _go():
        _t.sleep(delay)
        system = platform.system()
        try:
            if system == "Windows":
                # os.startfile opens URLs via the default browser silently
                os.startfile(url)  # noqa: S606
            elif system == "Darwin":
                subprocess.Popen(["open", url],
                                 stdout=subprocess.DEVNULL,
                                 stderr=subprocess.DEVNULL)
            else:
                subprocess.Popen(["xdg-open", url],
                                 stdout=subprocess.DEVNULL,
                                 stderr=subprocess.DEVNULL)
        except Exception:
            # Fallback — webbrowser module (may show CMD on Windows but at least works)
            webbrowser.open(url)

    threading.Thread(target=_go, daemon=True).start()


