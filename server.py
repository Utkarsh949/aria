def _load_html() -> str:
    global _HTML_CACHE
    if _HTML_CACHE is not None:
        return _HTML_CACHE
    # 1. Explicit override via env var
    external = config.ARIA_UI_PATH
    if external:
        p = pathlib.Path(external)
        if p.exists():
            logger.info("Loading UI from external file: %s", p)
            _HTML_CACHE = p.read_text(encoding="utf-8")
            return _HTML_CACHE
        logger.warning("ARIA_UI_PATH set but file not found: %s", p)
    # 2. Auto-detect dashboard.html next to server.py
    auto = pathlib.Path(__file__).parent / "dashboard.html"
    if auto.exists():
        logger.info("Loading UI from dashboard.html")
        _HTML_CACHE = auto.read_text(encoding="utf-8")
        return _HTML_CACHE
    # 3. Fall back to embedded base64 UI
    _HTML_CACHE = base64.b64decode(_DASHBOARD_B64).decode("utf-8")
    return _HTML_CACHE


