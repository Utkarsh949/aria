@app.post("/webhook/crm", tags=["webhooks"])
async def crm_webhook(payload: dict, background_tasks: BackgroundTasks):
    """
    CRM webhook receiver — auto-routes to the right ARIA agent.

    Supported event_type values:
      deal.stage_changed | deal.updated
      account.health_changed | account.renewal_approaching
      deal.competitor_mentioned
    """
    event_type = payload.get("event_type", "")
    logger.info("Webhook: %s", event_type)
    aria = get_aria()

    if event_type in ("deal.stage_changed", "deal.updated"):
        background_tasks.add_task(_bg_deal, aria, payload.get("deal", {}))
    elif event_type in ("account.health_changed", "account.renewal_approaching"):
        background_tasks.add_task(_bg_retention, aria, payload.get("account", {}))
    elif event_type == "deal.competitor_mentioned":
        background_tasks.add_task(
            _bg_competitive, aria,
            payload.get("deal", {}), payload.get("competitor", ""),
        )

    return {"received": True, "event_type": event_type}


async def _bg_deal(aria: ARIA, deal: dict):
    if not deal:
        return
    r = await aria.deal_intelligence(deal)
    logger.info("BG deal: %s  confidence=%.2f", r.data.get("deal_id"), r.confidence)


async def _bg_retention(aria: ARIA, account: dict):
    if not account:
        return
    r = await aria.retention(account)
    logger.info("BG retention: %s  churn=%s",
                r.data.get("account_id"), r.data.get("churn_risk_score"))


async def _bg_competitive(aria: ARIA, deal: dict, competitor: str):
    if not deal or not competitor:
        return
    r = await aria.competitive_intel(deal, competitor)
    logger.info("BG competitive: deal=%s  vs=%s", r.data.get("deal_id"), competitor)


